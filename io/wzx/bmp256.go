func newBmp256Header(size uint32) *BmpHeader {
	header := BmpHeader{Size: size, Pos: 1024 + 14 + 40}
	header.Flag[0] = 0x42
	header.Flag[1] = 0x4D
	return &header
}
func newBmp256Info(w, h uint32) *BmpInfo {
	info := BmpInfo{Size: 40,
		Width:    w,
		Height:   h,
		Planes:   1,
		BitCount: 8,
	}
	return &info
}
